/*for*/
//tres expressoes em uma unica


#include <stdio.h>
#include <math.h>

int main()
{
    int conta; //não precisa inicializar a variável pq a expressão já inicia
    for (conta = 0; conta <= 10; conta ++)
        printf("CONTA=%d\n", conta);
    return 0;
}

